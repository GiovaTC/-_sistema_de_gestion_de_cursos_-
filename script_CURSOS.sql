CREATE TABLE CURSOS (
    ID_CURSO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100),
    PROFESOR VARCHAR2(100),
    CREDITOS NUMBER,
    CONSTRAINT PK_CURSO PRIMARY KEY(ID_CURSO)
);

CREATE TABLE ESTUDIANTES (
    ID_ESTUDIANTE NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100),
    EMAIL VARCHAR2(100),
    CONSTRAINT PK_ESTUDIANTE PRIMARY KEY(ID_ESTUDIANTE)
);

CREATE TABLE INSCRIPCIONES (
    ID_INSCRIPCION NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_ESTUDIANTE NUMBER,
    ID_CURSO NUMBER,
    FECHA_INSCRIPCION DATE DEFAULT SYSDATE,

    CONSTRAINT PK_INSCRIPCION PRIMARY KEY(ID_INSCRIPCION),

    CONSTRAINT FK_ESTUDIANTE
    FOREIGN KEY (ID_ESTUDIANTE)
    REFERENCES ESTUDIANTES(ID_ESTUDIANTE),

    CONSTRAINT FK_CURSO
    FOREIGN KEY (ID_CURSO)
    REFERENCES CURSOS(ID_CURSO)
);

COMMIT;
